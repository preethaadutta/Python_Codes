#WAP to reverse a tuple using function
tuple1=()
list1=list(tuple1)
n1=int(input("Enter number of elements in list:"))
for i in range(0,n1):
    element1=int(input())
    list1.append(element1)
list1.reverse()
print("Reverse of the list:",list1)

